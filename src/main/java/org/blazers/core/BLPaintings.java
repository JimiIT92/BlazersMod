    /**
     * Register a {@link PaintingVariant painting} sized 16x16
     *
     * @param name {@link String Painting Name}
     * @return {@link PaintingVariant Registered Painting}
     */
    private static PaintingVariant registerSmallPainting(final String name) {
        return registerPainting(name, 16);
    }

    /**
     * Register a {@link PaintingVariant painting} with the same
     * {@link Integer Width} and {@link Integer Height}
     *
     * @param name {@link String Painting Name}
     * @param size {@link Integer Painting Size}
     * @return {@link PaintingVariant Registered Painting}
     */
    private static PaintingVariant registerPainting(final String name, final int size) {
        return registerPainting(name, size, size);
    }

    /**
     * Register a {@link PaintingVariant painting}
     *
     * @param name {@link String Painting Name}
     * @param width {@link Integer Painting Width}
     * @param height {@link Integer Painting Height}
     * @return {@link PaintingVariant Registered Painting}
     */
    private static PaintingVariant registerPainting(final String name, final int width, final int height) {
        return register(name, new PaintingVariant(width, height));
    }

    private static PaintingVariant register(String name, PaintingVariant variant) {
        return Registry.register(Registries.PAINTING_VARIANT, new Identifier(BlazersMod.MOD_ID, name), variant);
    }

    public static void register() {
        BlazersMod.LOGGER.info("Registering " + BlazersMod.MOD_ID + " paintings");
    }
